X509* get_cert(int id){
    X509 *cert = NULL;
    unsigned char* buf = NULL;
    int len = 0;
    BIO *bp = NULL;
    switch (id) {
        case CERTSTORE_MASTER:
            buf = (unsigned char*) master_pem;
            len = master_pem_len;
            break;
        case CERTSTORE_REALM:
            buf = (unsigned char*) realm_pem;
            len = realm_pem_len;
            break;
        case CERTSTORE_RANDOM:
            buf = (unsigned char*) random_pem;
            len = random_pem_len;
            break;
        case CERTSTORE_DEVICE:
            buf = (unsigned char*) device_pem;
            len = device_pem_len;
            break;
        default:
            break;
    }

    fail_if(buf == NULL);
    fail_if(len <= 0);
    bp = BIO_new_mem_buf(buf, len);
    fail_if(bp == NULL);
    cert = PEM_read_bio_X509(bp, NULL, 0, NULL);
    BIO_free(bp);
    fail_if(cert == NULL);
    return cert;
}

STACK_OF(X509) *get_cert_store(int *ids){
    STACK_OF(X509) *store = sk_X509_new(NULL);
    while(ids && ((*ids) != -1)){
        sk_X509_push(store, get_cert(*ids));
        ids++;
    }
    return store;
}

